  <h3>Important information for rejected contributions</h3>

  <p>
    If you’ve had a Super Guarantee contribution returned to you since the end
    of October 2023, it may be due to a failed verification check.
  </p>
  <p>
    From 1 November 2023, our system has been updated with more robust checks to
    verify member information. The most common mistake leading to returned
    contribution are
    <strong> typos in the name and/or date of birth, </strong>
    and members who have legally changed their name but are yet to update their super
    account.
  </p>
