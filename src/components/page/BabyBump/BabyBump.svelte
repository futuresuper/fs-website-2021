<section>
  <p>
    Baby Bump is a refund of the annual dollar-based administration fee for the
    time you are on parental leave, up to a maximum of 12 months (equivalent to
    $60).
  </p>
  <p>We created Baby Bump for two reasons:</p>
  <ul>
    <li>Employers don't have to pay super when you take parental leave</li>
    <li>
      Primary carers (who are historically mostly women) are more likely to take
      parental leave than other carers
    </li>
  </ul>
  <p>
    These two factors contribute to the startling gender inequality in super:
    that, on average, women retire with 42%* less super than men. So Baby Bump
    is Future Super’s baby step towards making super more fair for our members.
  </p>
  <p>
    You don't have to be the birth parent to take advantage of our Baby Bump
    policy however. If you have welcomed a new child into your family and taken
    parental leave or left work to care for that child, you can apply for the
    Baby Bump regardless of your gender.
  </p>
  <p>
    See <a
      href="https://www.abc.net.au/radio/programs/nightlife/first-hour-finance/13310718"
      >Closing the super gender gap</a
    >.
  </p>
  <h2>Am I eligible?</h2>
  <p>You may be eligible for the Baby Bump fee refund if:</p>
  <ul>
    <li>You are a Future Super member</li>
    <li>
      A new child entered into your care within the past 18 months, whether by
      birth or adoption, or foster care
    </li>
    <li>
      You have taken parental leave or left work to care for your new child
    </li>
  </ul>
  <p>
    To confirm eligibility, we do require some paperwork to confirm the birth,
    adoption, or foster care of your new child. You can provide this
    documentation after submitting your Baby Bump application below.
  </p>
  <h2>When should I apply?</h2>
  <p>
    ‍A Baby Bump application should be submitted when you return to work after
    parental leave (or start working more than 10 hours per week).
  </p>
  <p>
    Making the switch to full-time parent? You can still apply! If you're not
    returning to work or planning to work more than 10 hours per week, apply for
    the full Baby Bump refund after the child has been in your care for 12
    months, or the child has left your care if you are a foster carer.
  </p>
  <p>
    Please note that Baby Bump applications must be received within 18 months of
    your new child's birth, adoption, or commencement of foster care.
  </p>
</section>

