  </p>
  <h3 id="2-data-breaches">
    <a name="_toc140395128" /><strong>2. Data breaches</strong>
  </h3>
  <p>
    Despite best endeavours data breaches with respect to personal information
    may occur from time to time. A data breach occurs when personal information
    an organisation holds is lost or subject to unauthorised access or
    disclosure. For example, when:
  </p>
  <ul>
    <li>A device with a customer’s personal information is lost or stolen</li>
    <li>A database with personal information is hacked</li>
    <li>Personal information is mistakenly given to the wrong person.</li>
  </ul>
  <h4 id="21-reporting-data-breaches---internal">
    <a name="_toc140395129" /><strong
      >2.1 Reporting data breaches - internal</strong
    >
  </h4>
  <p>
    All privacy breaches should be reported to the Future Group nominated
    Privacy Officer in accordance with the Incident and Breach Management Policy
    as soon as possible and ideally within 1 business day to allow for
    assessment and management of the breach.
  </p>
  <p>
    The Privacy Officer must take reasonable steps to complete a Notifiable Data
    Breach (<strong>NDB</strong>) assessment within 30 calendar days after the
    day we became aware of the breach or had reason to suspect an eligible data
    breach has occurred.
  </p>
  <h4 id="22-reporting-data-breaches--external">
    <a name="_toc140395130" /><strong
      >2.2 Reporting data breaches – external</strong
    >
  </h4>
  <p>
    The Privacy Act requires organisations to notify the Office of the
    Australian Information Commissioner (<strong>OAIC</strong>) when a privacy
    breach is assessed under the NDB scheme as being an ‘eligible data breach’.
    An eligible data breach occurs when:
  </p>
  <ul>
    <li>
      There is unauthorised access to or disclosure of personal information, or
      a loss of personal information, that an organisation holds
    </li>
    <li>
      This is likely to result in serious harm to one or more individuals, and
    </li>
    <li>
      The organisation hasn’t been able to prevent the likely risk of serious
      harm through remedial action.
    </li>
  </ul>
  <p>
    Whether a data breach is likely to result in serious harm requires an
    objective assessment, determined from the viewpoint of a reasonable person
    within the Future Group. This assessment will be undertaken by the Privacy
    Officer using the checklist in <strong>Attachment 2</strong>. In general,
    entities are not expected to make external enquiries about the circumstances
    of each individual whose information is involved in the breach.
  </p>
