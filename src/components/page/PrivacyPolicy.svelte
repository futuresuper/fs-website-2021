  <h4 id="austrac-guidance"><strong>AUSTRAC guidance</strong></h4>
  <h4 id="the-type-of-personal-information">
    <strong>The type of personal information</strong>
  </h4>
  <p>
    Some kinds of personal information may be more likely to cause an individual
    serious harm if compromised. Examples of the kinds of information that may
    increase the risk of serious harm if there is a data breach include:
  </p>
  <ul>
    <li>
      ‘sensitive information’, (guidance can be found at <a
        href="https://www.oaic.gov.au/privacy/guidance-and-advice/data-breach-preparation-and-response/part-4-notifiable-data-breach-ndb-scheme#ftn24"
        >**[24]</a
      ><strong>)</strong> such as information about an individual’s health
    </li>
    <li>
      documents commonly used for identity fraud (including Medicare card,
      driver licence, and passport details)
    </li>
    <li>financial information</li>
    <li>
      a combination of types of personal information (rather than a single piece
      of personal information) that allows more to be known about the
      individuals the information is about.
    </li>
  </ul>
  <h4 id="the-circumstances-of-the-breach">
    <strong>The circumstances of the breach</strong>
  </h4>
  <p>
    The specific circumstances of the data breach are relevant when assessing
    whether there is a risk of serious harm to an individual. This may include
    consideration of the following:
  </p>
  <ul>
    <li>
      <strong>Whose personal information was involved in the breach?</strong> An
      entity could consider whose personal information was involved in the breach,
      as certain people may be at particular risk of serious harm. A data breach
      involving the names and addresses of individuals might not, in various circumstances,
      be likely to result in serious harm to an individual, particularly if that
      information is already publicly available. However, if the entity knows that
      the information involved primarily relates to individuals known to be vulnerable,
      this may increase the risk of serious harm.
    </li>
    <li>
      <strong>How many individuals were involved?</strong> If the breach involves
      the personal information of many individuals, the scale of the breach should
      affect an entity’s assessment of likely risks. Even if an entity considers
      that each individual will only have a small chance of suffering serious harm,
      if more people’s personal information is involved in the breach, it may be
      more likely that at least some of the individuals will experience serious harm.
      From a risk perspective, it may be prudent, depending on the particular circumstances,
      to assume a breach involving the personal information of a very large number
      of people is likely to result in serious harm to at least one of those individuals,
      unless context or circumstances would support this not being the case.
    </li>
    <li>
      <strong
        >Do the circumstances of the data breach affect the sensitivity of the
        personal information?</strong
      >
      A breach that may publicly associate an individual’s personal information with
      a sensitive product or service they have used may increase the risk of serious
      harm. For example, a data breach involving an individual’s name may involve
      a risk of serious harm if the entity’s name links the individual with a particular
      form of physical or mental health care. (Guidance can be found at
      <a
        href="https://www.oaic.gov.au/privacy/guidance-and-advice/data-breach-preparation-and-response/part-4-notifiable-data-breach-ndb-scheme#ftn26"
        ><strong>[26]</strong></a
      >).
    </li>
    <li>
      <strong>How long has the information been accessible?</strong> The time between
      when the data breach occurred and when the entity discovers the breach will
      be relevant to the entity’s consideration of whether serious harm is likely
      to occur. For example, if personal information is publicly accessible for a
      significant period before the entity is aware of the data breach, it may be
      more likely that the personal information have been accessed in ways that will
      result in serious harm to the individuals affected.
    </li>
    <li>
      <strong
        >Is the personal information adequately encrypted, anonymised, or
        otherwise not easily accessible?</strong
      > A relevant consideration is whether the information is rendered unreadable
      through the use of security measures to protect the stored information, or
      if it is stored in such a way so that it cannot be used if breached. In considering
      whether security measures (such as encryption) applied to compromised data
      are adequate, the entity should consider whether the method of encryption is
      an industry-recognised secure standard at the time the entity is assessing
      the likelihood of risk. Additionally, an entity should have regard to whether
      the unauthorised recipients of the personal information would have the capability
      to circumvent these safeguards. For example, if an attacker holds both encrypted
      data and the encryption key needed to decrypt that data, the entity should
      not assume the data is secure.
    </li>
    <li>
      <strong
        >What parties have gained or may gain unauthorised access to personal
        information?</strong
      > The unauthorised disclosure of an individual’s criminal record to someone
      who knows that individual personally may increase the risk of serious reputational
      harm for that individual. In addition, where a third party that obtains unauthorised
      access to personal information or appears to target personal information of
      a particular individual or group of individuals, this may increase the risk
      of serious harm as it may be more likely the personal information is intended
      for malicious purposes.
    </li>
  </ul>
  <h4 id="the-nature-of-the-harm"><strong>The nature of the harm</strong></h4>
  <p>
    In assessing the risk of serious harm, entities should consider the broad
    range of potential kinds of harms that may follow a data breach. It may be
    helpful for entities assessing the likelihood of harm to consider a number
    of scenarios that would result in serious harm and the likelihood of each.
    Examples may include:
  </p>
  <ul>
    <li>identity theft</li>
    <li>significant financial loss by the individual</li>
    <li>threats to an individual’s physical safety</li>
    <li>loss of business or employment opportunities</li>
    <li>humiliation, damage to reputation or relationships</li>
    <li>workplace or social bullying or marginalisation</li>
  </ul>
  <p>
    The likelihood of a particular harm occurring, as well as the anticipated
    consequences for individuals whose personal information is involved in the
    data breach if the harm materialises, are relevant considerations.
  </p>
  <h4 id="preventing-serious-harm"><strong>Preventing serious harm</strong></h4>
  <p>
    If an entity takes remedial action such that the data breach would not be
    likely to result in serious harm, then the breach is not an eligible data
    breach for that entity or for any other entity (see s26WF(1), s26WF(2),
    s26WF(3)).
  </p>
