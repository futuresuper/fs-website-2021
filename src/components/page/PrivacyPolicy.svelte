  <h3 id="1-overview">
    <a name="_toc140395123" /><strong>1. Overview</strong>
  </h3>
  <h4 id="" />
  <h4 id="11-objective-and-application">
    <a name="_toc140395124" /><strong>1.1 Objective and Application</strong>
  </h4>
  <p>
    This Privacy Policy (<strong>Policy</strong>) applies to Future
    Superannuation Group Pty Ltd (<strong>FSG</strong> ABN 68 618 367 927) and
    its related bodies corporate (together referred to in this document as
    <strong>Future Group</strong>, , <strong>we</strong> or
    <strong>us)</strong>.
  </p>
  <p>
    Future Group understand the importance of privacy and the security of
    personal information and is bound by the Privacy Act 1988 (cth) (<strong
      >Act</strong
    >) and the Australian Privacy Principles (<strong>APP</strong>s). To ensure
    compliance with the Privacy Act and as part of our commitment to ensuring
    the security of private and confidential information, we have established
    and implemented this Policy.
  </p>
  <h4 id="12-relevant-legislation">
    <a name="_toc140395125" /><strong>1.2 Relevant Legislation</strong>
  </h4>
  <p>
    The Privacy Act applies to organisations with an annual turnover of more
    than $3 million operating in Australia and requires that we handle personal
    information in accordance with a set of national principles, known as the
    APPs. The APPs regulate the collection, use, correction, disclosure and
    transfer of personal information about individuals.
  </p>
  <p>
    APP 1 requires organisations to manage personal information in an open and
    transparent way and to have a clearly expressed and up to date privacy
    policy about how it manages personal information. This policy must be easily
    understood and freely available and in an appropriate form, usually on its
    website. Accordingly, <strong>Attachment 1</strong> has been prepared to meet
    these obligations and will be made available on the website of all companies
    within the Future Group.
  </p>
  <p>
    The General Data Protection Regulations (<strong>GDPR</strong>) in the
    European Union and the United Kingdom impose similar obligations to
    organisations operating in those regions to ensure personal information is
    used fairly, lawfully and transparently. While we have taken steps to align
    with GDPR we do not comply with these regimes as we do not operate in those
    regions.
  </p>
  <h4 id="13-awareness-and-understanding">
    <a name="_toc140395126" /><strong>1.3 Awareness and understanding</strong>
  </h4>
  <p>
    A copy of this Policy is made available to all staff of Future Group on a
    shared drive. If material changes are made to this Policy, these changes are
    communicated to all staff by the most appropriate means in the given
    circumstances, and a copy of the revised document is made available.
  </p>
  <p>
    Training is provided to staff to ensure they are aware of their
    responsibilities.
  </p>
  <p>
    Persistent and conscious failure by staff members to act in accordance with
    this Policy is considered a serious disciplinary matter and corrective
    action will be determined by Future Group on a case-by-case basis.
  </p>
