<div>
  <h3 id="future-group-privacy-policy">Future Group Privacy Policy</h3>
  <p><strong>Updated August 2023</strong></p>
  <h3 id="1-overview-1">1. <strong>Overview</strong></h3>
  <p>
    This Privacy Policy (<strong>Policy</strong>) applies to Future
    Superannuation Group Pty Ltd and its related bodies corporate (<strong
      >Future Group,</strong
    > <strong>we</strong>, <strong>us</strong>,** and **our**) including Future
    Super and smartMonday. It outlines how we manage your personal information
    and safeguard your privacy.
  </p>
  <p>
    At Future Group, we understand your concerns about privacy and the security
    of your personal information. Your privacy is important to us, and we are
    bound by the Privacy Act 1988 (Cth) (the <strong>Act</strong>) and the
    Australian Privacy Principles (’<strong>APP</strong>s’). We are committed to
    protecting the personal information that we hold about you.
  </p>
  <p>
    To ensure compliance with the Privacy Act and as part of our commitment to
    ensuring the security of your private and confidential information, we have
    established and implemented this Policy.
  </p>
  <p>Future Group will:</p>
  <ul>
    <li>take reasonable steps to protect your information.</li>
    <li>
      comply with the notification requirements of the Act if we are aware of
      your information being lost or compromised.
    </li>
    <li>never sell your information to a third party.</li>
    <li>
      use information we collect from you and from other sources to provide
      services to you and to improve how we operate and provide those services.
    </li>
  </ul>
  <h3 id="2-future-group-activities">
    2. <strong>Future Group Activities</strong>
  </h3>
  <p>Future Group provides:</p>
  <ul>
    <li>
      administration, sponsor and promotional activities on behalf of
      superannuation funds;
    </li>
    <li>financial product advice to superannuation fund members; and</li>
    <li>
      investment management and consulting activities on behalf of institutional
      clients
    </li>
  </ul>
  <h3 id="3-collecting-your-personal-information">
    3. <strong>Collecting your personal information</strong>
  </h3>
  <h4 id="31-what-is-personal-information">
    <strong>3.1 What is personal information?</strong>
  </h4>
  <p>
    Personal information is information or an opinion that allows others to
    identify you. This includes your name, gender, contact details, as well as
    your health and financial information.
  </p>
  <h4 id="32-why-do-we-collect-hold-and-use-your-personal-information">
    <strong
      >3.2 Why do we collect, hold and use your personal information?</strong
    >
  </h4>
  <p>
    We will only collect personal information that is reasonably necessary to
    offer and administer our services and products, or to provide information
    about a service or product or any other information that you have requested
    from us. We will collect personal information only by lawful and fair means.
  </p>
  <p>We may also collect personal information to:</p>
  <ul>
    <li>identify and interact with you;</li>
    <li>
      open and administer your super and retirement accounts, including any
      insurance entitlements, and keep you informed about your accounts and
      entitlements and opportunities available to you as a member;
    </li>
    <li>
      assess your eligibility for products and services (including insurance)
      and events;
    </li>
    <li>
      help you combine your super accounts and check if you have lost super
      (when you ask us to);
    </li>
    <li>provide advice and other services to you;</li>
    <li>help improve our products and services;</li>
    <li>manage and resolve complaints;</li>
    <li>conduct market or customer satisfaction research;</li>
    <li>
      develop, establish and administer alliances and other arrangements with
      other organisations in relation to the promotion, administration and use
      of our respective products and services;
    </li>
    <li>
      comply with any legal requirements, including any purpose authorised or
      required by an Australian law, court or tribunal; or
    </li>
    <li>for any other purpose for which you give your consent.</li>
  </ul>
  <p>
    If you are not a customer but interact with Future Group, we may collect
    information to identify and interact with you.
  </p>
  <p>
    If you are a contractor or prospective staff member, we may collect
    information to allow us to carry out our recruitment functions, to
    correspond with you, to fulfil the terms of any contractual relationship
    and/or to ensure that you can perform your duties.
  </p>
  <h4 id="33-what-type-of-information-can-we-collect-from-you">
    <strong>3.3 What type of information can we collect from you?</strong>
  </h4>
  <p><strong>Personal information</strong></p>
  <p>
    The type of information we collect will depend on the type of product or
    service you request. Examples of personal information we collect includes:
  </p>
  <ul>
    <li>name</li>
    <li>contact details</li>
    <li>date of birth</li>
    <li>gender</li>
    <li>tax file number (TFN)</li>
    <li>bank account details</li>
    <li>
      Technical and usage eg IP address, cookies (when you use our website)
    </li>
  </ul>
  <p><strong>Sensitive information</strong></p>
  <p>
    For some of our products and services, we may also need to collect sensitive
    information about you where the information is necessary for one or more of
    our functions or activities and you consent to the collection, or it is
    required or authorized by law. This may include:
  </p>
  <ul>
    <li>
      Health information eg if you apply for insurance cover, or for the purpose
      of assessing an entitlement;
    </li>
    <li>Photographic identity documents for customer identification; and</li>
    <li>
      information about your criminal convictions, political affiliation,
      membership of professional or trade associations and sexual preferences.
    </li>
  </ul>
  <p>
    Sensitive information will only be used for the purpose for which it was
    provided.
  </p>
  <p>
    If you provide us with information about other individuals (such as
    employees, dependents etc.) you must obtain their consent for us to use
    their information in accordance with our Privacy Policy prior to your
    disclosure to us or otherwise let us know if this is not the case.
  </p>
  <h4
    id="34-what-happens-if-you-dont-provide-us-with-your-information-or-provide-incomplete-or-inaccurate-information"
  >
    <strong
      >3.4 What happens if you don’t provide us with your information, or
      provide incomplete or inaccurate information?</strong
    >
  </h4>
  <p>
    You do not have to give us your personal information. However, if you choose
    not to, we may not be able to provide you with our products or services or
    more information about them. We might not be able to process your
    transactions or allow you to access our online services.
  </p>
  <p>
    If you do not provide the information we request, you would like to remain
    anonymous or you want to use a pseudonym, we might not be able to provide
    the appropriate type or level of service or product.
  </p>
  <h4 id="35-how-do-we-collect-your-information">
    <strong>3.5 How do we collect your information?</strong>
  </h4>
  <p>
    Unless impracticable or unreasonable to do so, we will collect information
    directly from you. This might be when you contact us by telephone, email,
    letter or online in order to complete a product or service application, or
    any other kind of administrative form. Our emails may contain automatic
    features that collect personal information. You can engage with us through
    social media or our website, or through our mobile phone app.
  </p>
  <p>
    If we are permitted by law to do so, we may collect information about you
    from third parties. We may collect this information from third parties such
    as our affiliates, Authorised Representatives, or other third parties such
    as employers, insurance companies, financial institutions, medical
    professionals, law enforcement, dispute resolution, statutory and regulatory
    bodies, marketing lists and industry databases, publicly available sources
    or other government bodies.
  </p>
  <p>
    If you ask us we will take reasonable steps to let you know how we have
    sourced your personal information, unless it is obvious from the
    circumstances that you would know or would reasonably expect us to have the
    information (such as where we are dealing with your advisers).
  </p>
  <h4 id="36-how-do-we-notify-you-and-obtain-your-consent">
    <strong>3.6 How do we notify you and obtain your consent?</strong>
  </h4>
  <p>
    In most cases, we will obtain your consent to handle your personal
    information either at the time you engage us to provide you with a product
    or service, or as soon as practicable. Consent can be either express or
    implied. Where consent is requested, we will properly inform you what you
    are consenting to.
  </p>
  <h4 id="37-modifying-your-consent-and-opting-out-of-marketing">
    <strong>3.7 Modifying your consent and opting-out of marketing</strong>
  </h4>
  <p>
    We may use your personal information to contact you to promote and market
    our products and services. You may modify or withdraw your consent, or
    opt-out of receiving direct marketing at any time by contacting the Privacy
    Officer (<a href="mailto:privacy@futuregroup.com.au"
      >privacy@futuregroup.com.au</a
    >).
  </p>
  <p>
    If you receive electronic communications, such as an e-newsletter, you may
    unsubscribe at any time by following the instructions included in the
    communication.
  </p>
  <p>
    If you previously chose to receive push notifications on your mobile device,
    you can manage your preferences either through your device or the
    application settings. Alternatively, you may uninstall the application by
    using the uninstall process available on your mobile device.
  </p>
  <p>
    To prevent the use of cookies and the associated advertising, you need to
    adjust the settings on your browser to refuse all cookies.
  </p>
  <p>
    Once we receive a request to opt out from receiving marketing information,
    we will stop sending such information within a reasonable amount of time.
  </p>
  <h4 id="38-how-do-we-deal-with-unsolicited-information">
    <strong>3.8 How do we deal with unsolicited information?</strong>
  </h4>
  <p>
    Where we receive information that we have not requested (“unsolicited
    information”), we will determine whether we could have legally collected the
    information, and whether it is reasonably necessary for our functions or
    activities. If it is, we will handle the information in the same way that we
    handle information we have requested. If not, we will take steps to destroy
    or de-identify the information.
  </p>
  <h3 id="4-using-and-disclosing-your-personal-information">
    4. <strong>Using and disclosing your personal information</strong>
  </h3>
  <p>
    We will generally only use and disclose your personal information for the
    purpose that it was collected for, any related purpose that you would
    reasonably expect us to use or disclose it for, for the purpose of
    analytics, or as permitted under this Privacy Policy or under any law.
    Future Group otherwise has a duty to maintain the confidentiality of its
    clients’ information unless disclosure is permitted with your consent or
    compelled under any law.
  </p>
  <h4 id="41-using-your-personal-information">
    <strong>4.1 Using your personal information</strong>
  </h4>
  <p>Your information may be used for the following purposes to:</p>
  <ul>
    <li>provide the information, products or services you requested;</li>
    <li>
      determine your eligibility and process applications for products and
      services you have requested;
    </li>
    <li>provide information and services you requested;</li>
    <li>
      understand and assess your ongoing needs, and to offer products and
      services to meet those needs;
    </li>
    <li>administer insurance and benefit payment claims;</li>
    <li>monitor and train;</li>
    <li>develop new services;</li>
    <li>manage a complaint;</li>
    <li>comply with our legal obligations; and</li>
    <li>market products and services.</li>
  </ul>
  <p>
    We may conduct data analysis and data analytics on customer information we
    hold. Generally, this information is based on aggregated data that doesn’t
    contain information that identifies individuals.
  </p>
  <h4 id="42-disclosing-your-personal-information">
    <strong>4.2 Disclosing your personal information</strong>
  </h4>
  <p>
    We may disclose your information to our affiliates or third party service
    providers to assist us in providing, managing and administering our services
    and products, such as:
  </p>
  <ul>
    <li>
      with any third party on specific matters if you have expressly consented
      to the disclosure;
    </li>
    <li>our Authorised Representatives;</li>
    <li>to your beneficiaries or dependents in the event of your death;</li>
    <li>
      with service providers/third parties that we need to deal with for the
      purposes described in section 3.2 including: superannuation
      administrators, insurers, your employer, identity verification agencies,
      mail houses, auditors, actuaries and lawyers, web hosting companies, web
      and application developers, other superannuation funds and administrators
      if you transfer your investment to another fund, and external IT service
      providers, infrastructure and other third parties where required by law.
    </li>
    <li>
      Law enforcement and fraud prevention agencies (for example the police)
      that may require disclosure on legal grounds.
    </li>
  </ul>
  <p>
    We will only use and disclose your sensitive information for the purpose it
    was collected, for any other directly related purpose that you would
    reasonably expect us to use it for, or in accordance with a law or
    regulation. With your consent, we may use or disclose your information for
    additional purposes from time to time.
  </p>
  <p>
    In addition to our affiliates, we may also disclose personal information to
    third parties such as our contractors, agents and service providers when we
    outsource certain functions, including market research, marketing, claims
    handling and recruitment. This would also include our third party storage
    providers whom we may use from time to time to store information physically
    or electronically.
  </p>
  <h4 id="43-overseas-disclosure-of-your-personal-information">
    <strong>4.3 Overseas disclosure of your personal information</strong>
  </h4>
  <p>
    Some of our contractors, agents and service providers are located outside of
    Australia, meaning your personal information may be disclosed to a recipient
    in other countries. We limit the use and disclosure of personal information
    to the purpose for which it was provided.
  </p>
  <p>
    In addition, anonymised web traffic information is disclosed to third party
    analytics platforms when you visit our websites (who may store the
    information across multiple countries) and when you communicate with us
    through our social media applications, where the provider and its partners
    may collect and hold your personal information overseas.
  </p>
  <p>
    We will not disclose your personal information to overseas recipients unless
    and until we have taken reasonable steps to ensure that the overseas
    recipient will handle your personal information in a manner consistent with
    the APPs, or you have consented to the transfer of the information.
  </p>
  <h4 id="44-holding-and-retaining-your-personal-information">
    <strong>4.4 Holding and retaining your personal information</strong>
  </h4>
  <p>
    We take reasonable precautions to protect and secure your personal
    information from misuse, interference, loss, unauthorised access,
    modification or unauthorised disclosure.
  </p>
  <p>
    Your personal information is held by us electronically on secure systems
    provided by cloud service providers. It may also be held in hardcopy form,
    for example, in paper files which are archived in a secure facility.
    Personal information held electronically is secured by passwords, encryption
    and firewalls in line with Australian information security standards. We
    undertake audits and monitoring of staff access to our systems.
  </p>
  <p>
    Your personal information is only retained for as long as necessary to
    fulfil the purpose/s for which it was collected, unless we are required to
    retain the information for a certain period of time under an Australian law
    or court or tribunal order. Once no longer required, we take such steps as
    are reasonable in the circumstances to destroy or permanently de-identify
    your personal information.
  </p>
