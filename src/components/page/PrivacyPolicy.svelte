    <strong
      >3.2 Why do we collect, hold and use your personal information?</strong
    >
  </h4>
  <p>
    We will only collect personal information that is reasonably necessary to
    offer and administer our services and products, or to provide information
    about a service or product or any other information that you have requested
    from us. We will collect personal information only by lawful and fair means.
  </p>
  <p>We may also collect personal information to:</p>
  <ul>
    <li>identify and interact with you;</li>
    <li>
      open and administer your super and retirement accounts, including any
      insurance entitlements, and keep you informed about your accounts and
      entitlements and opportunities available to you as a member;
    </li>
    <li>
      assess your eligibility for products and services (including insurance)
      and events;
    </li>
    <li>
      help you combine your super accounts and check if you have lost super
      (when you ask us to);
    </li>
    <li>provide advice and other services to you;</li>
    <li>help improve our products and services;</li>
    <li>manage and resolve complaints;</li>
    <li>conduct market or customer satisfaction research;</li>
    <li>
      develop, establish and administer alliances and other arrangements with
      other organisations in relation to the promotion, administration and use
      of our respective products and services;
    </li>
    <li>
      comply with any legal requirements, including any purpose authorised or
      required by an Australian law, court or tribunal; or
    </li>
    <li>for any other purpose for which you give your consent.</li>
  </ul>
  <p>
    If you are not a customer but interact with Future Group, we may collect
    information to identify and interact with you.
  </p>
  <p>
    If you are a contractor or prospective staff member, we may collect
    information to allow us to carry out our recruitment functions, to
    correspond with you, to fulfil the terms of any contractual relationship
    and/or to ensure that you can perform your duties.
