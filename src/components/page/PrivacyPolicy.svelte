  <h3 id="6-privacy-online">6. <strong>Privacy online</strong></h3>
  <p>
    The internet is not a secure environment. If you use the internet to send us
    personal information you do so at your own risk.
  </p>
  <h4 id="61-website-cookies"><strong>6.1 Website cookies</strong></h4>
  <p>
    We make use of “cookies” technology on our websites. We use cookies to
    distinguish you from other users of our websites, improve the functionality
    of our websites and to learn more about the way that users interact with our
    content.
  </p>
  <p>
    You can disable the use of cookies through your internet browser
    preferences. Please note, if you choose to block all cookies you may not be
    able to access all or a part of our websites and may be unable to engage in
    some activities.
  </p>
  <h4 id="62-web-beacons"><strong>6.2 Web beacons</strong></h4>
  <p>
    We may also use tracking technologies called web beacons to collect
    information about your visit to our sites. Like cookies, this technology
    enables us to track pages and content accessed and viewed by users on Future
    Group sites. Also, when we send HTML-formatted (as opposed to plain text)
    emails to our users, this technology may be embedded in such emails. Using
    web beacons allows us to determine whether emails have been opened or links
    were accessed, and identify aggregate trends, individual usage, and generate
    statistics about how our site is used.
  </p>
  <p>
    You can avoid web beacons by choosing to open emails in plain text. You can
    manage these settings through your email application.
  </p>
  <h4 id="63-remarketing"><strong>6.3 Remarketing</strong></h4>
  <p>
    Our websites use the Google AdWords remarketing service and similar services
    from other providers to advertise on third party websites (including Google)
    to previous visitors to our sites. This could be in the form of an
    advertisement on the Google search results page, or a site in the Google
    Display Network, or other sites. Third-party vendors, including Google, may
    distribute ads based on someone’s past visits to the Future Super websites.
  </p>
  <p>
    Of course, any data collected will be used in accordance with our own
    privacy policy, Google’s privacy policy or the privacy policy of other
    remarketing services used by Future Super.
  </p>
  <h4 id="64-links-to-other-websites">
    <strong>6.4 Links to other websites</strong>
  </h4>
  <p>
    You may be able to access external websites by clicking on links provided on
    Future Group websites. These websites aren’t subject to our privacy
    standards and procedures. Once you leave our websites, our privacy policy no
    longer applies. You’ll need to review those websites directly to view a copy
    of their privacy policies and to ensure your personal information is
    protected.
  </p>
  <h4 id="65-social-media-applications">
    <strong>6.5 Social media applications</strong>
  </h4>
  <p>
    Whilst we may use social networking platforms for communications, we won’t
    ask you to supply personal information publicly over these platforms.
    Sometimes we’ll invite you to send your details to us via private messaging,
    for example, to answer a question you have raised about your superannuation
    account.
  </p>
  <p>
    Any personal information that you contribute to the publicly accessible
    sections of social media applications can be read, collected and used by
    other users of the application. We have no control over these other users
    and cannot guarantee that any information that you contribute to any social
    media applications will be handled in accordance with our privacy standards.
  </p>
