    of your personal information. Your privacy is important to us, and we are
    bound by the Privacy Act 1988 (Cth) (the <strong>Act</strong>) and the
    Australian Privacy Principles (’<strong>APP</strong>s’). We are committed to
    protecting the personal information that we hold about you.
  </p>
  <p>
    To ensure compliance with the Privacy Act and as part of our commitment to
    ensuring the security of your private and confidential information, we have
    established and implemented this Policy.
  </p>
  <p>Future Group will:</p>
  <ul>
    <li>take reasonable steps to protect your information.</li>
    <li>
      comply with the notification requirements of the Act if we are aware of
      your information being lost or compromised.
    </li>
    <li>never sell your information to a third party.</li>
    <li>
      use information we collect from you and from other sources to provide
      services to you and to improve how we operate and provide those services.
