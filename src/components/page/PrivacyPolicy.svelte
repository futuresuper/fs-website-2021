    3. <strong>Collecting your personal information</strong>
  </h3>
  <h4 id="31-what-is-personal-information">
    <strong>3.1 What is personal information?</strong>
  </h4>
  <p>
    Personal information is information or an opinion that allows others to
    identify you. This includes your name, gender, contact details, as well as
    your health and financial information.
  </p>
