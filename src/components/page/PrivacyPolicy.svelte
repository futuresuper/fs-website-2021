  <h3 id="attachment-2--eligible-data-breach-assessment-checklist">
    <a name="_toc140395139" /><strong
      >Attachment 2 – Eligible Data Breach Assessment Checklist</strong
    >
  </h3>
  <p>
    Use this checklist to assess whether a privacy breach meets the threshold to
    be considered a Notifiable Data Breach (<strong>NDB</strong>), also known as
    an Eligible Data Breach, in accordance with part IIIC of the Act. See
    AUSTRAC guidance here:
    <a
      href="https://www.oaic.gov.au/privacy/guidance-and-advice/data-breach-preparation-and-response/part-4-notifiable-data-breach-ndb-scheme#identifying-eligible-data-breaches"
      >https://www.oaic.gov.au/privacy/guidance-and-advice/data-breach-preparation-and-response/part-4-notifiable-data-breach-ndb-scheme#identifying-eligible-data-breaches</a
    >.
  </p>
  <p>An NDB occurs when:</p>
  <ol>
    <li>
      There is unauthorised access to or unauthorised disclosure of personal
      information, or a loss of personal information, that an entity holds, and
    </li>
    <li>
      This is likely to result in serious harm to one or more individuals, and
    </li>
    <li>
      The entity has been unable to prevent the likely risk of serious harm with
      remedial action
    </li>
  </ol>
  <p>
    Serious harm is not defined in the Privacy Act. In the context of a data
    breach, serious harm to an individual may include serious physical,
    psychological, emotional, financial, or reputational harm. The risk of
    serious harm should be assessed holistically having regard to the kind of
    information, the sensitivity of the information, whether the information is
    protected by security measures, the person/s who have or could obtain the
    information, and other relevant matters with respect to the data breach.
  </p>
  <p>
    EDB assessment is undertaken by the Privacy Officer to determine whether <em
      >a reasonable person would believe there might be risk of serious harm to
      an individual</em
    > whose personal information was part of the data breach.
  </p>
